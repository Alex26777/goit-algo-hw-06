import networkx as nx
import matplotlib.pyplot as plt

# Створення пустого графа
G = nx.Graph()

# Додавання вершин (станцій) до графа
станції = ["Станція 1", "Станція 2", "Станція 3", "Станція 4", "Станція 5"]
G.add_nodes_from(станції)

# Додавання ребер (з'єднань між станціями) до графа
зєднання = [("Станція 1", "Станція 2"), ("Станція 2", "Станція 3"), ("Станція 3", "Станція 4"), 
            ("Станція 4", "Станція 5"), ("Станція 5", "Станція 1"), ("Станція 2", "Станція 4")]
G.add_edges_from(зєднання)

# Візуалізація графа
plt.figure(figsize=(10, 7))
nx.draw(G, with_labels=True, node_color='skyblue', node_size=700, edge_color='k', linewidths=1, font_size=15)
plt.title("Модель транспортної мережі міста")
plt.show()

# Аналіз основних характеристик графа
print(f"Кількість вершин у графі: {G.number_of_nodes()}")
print(f"Кількість ребер у графі: {G.number_of_edges()}")
print("Ступінь кожної вершини:")
for станція in G.nodes:
    print(f"{станція}: {G.degree(станція)}")
